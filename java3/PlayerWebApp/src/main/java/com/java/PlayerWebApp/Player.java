package com.java.PlayerWebApp;

/**
 *
 * @author raksh
 */
public class Player {
    private String name;
    private String role;
    private String team;
    Player(String name, String role, String team){
        this.name=name;
        this.role=role;
        this.team=team;
        
        
        
    }

    Player() {

    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }

    /**
     * @return the team
     */
    public String getTeam() {
        return team;
    }

    /**
     * @param team the team to set
     */
    public void setTeam(String team) {
        this.team = team;
    }
}
